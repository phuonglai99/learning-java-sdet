package lab.lab_07_01;

public class Employee {
    private String name;
    private int salary;
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public int getSalary(){
        return this.salary;
    }
}
